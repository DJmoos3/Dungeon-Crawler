public class Object
{
    private int health;
    private int damage;
    private int armour;

    public void setHealth(int health) {
        this.health = health;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public void setArmour(int armour) {
        this.armour = armour;
    }

    public int getHealth() {
        return health;
    }

    public int getDamage() {
        return damage;
    }

    public int getArmour() {
        return armour;
    }
}
